async def _open_and_keep_alive(platform: str, account_name: str, account_file: Path) -> None:
    """Launch a headed browser and leave it open so the user can interact.
    Never closes — the user closes the window manually."""
    target_url = MESSAGING_URLS.get(platform, "")
    if not target_url:
        return

    pw = await async_playwright().start()
    browser = await pw.chromium.launch(**launch_kwargs(headless=False))
    context = await browser.new_context(
        storage_state=str(account_file), no_viewport=True
    )
    context = await _set_init_script(context)
    page = await context.new_page()
    await page.goto(target_url, timeout=30000)
    # Browser stays open; user interacts and closes it manually.
    # We intentionally never call browser.close() or pw.stop() here.


def open_messaging_page_sync(platform: str, account_name: str) -> dict:
    """Fire-and-forget: open messaging page in headed browser, return immediately."""
    cookie_path = account_file(platform, account_name)
    if not cookie_path.exists():
        return {
            "ok": False,
            "error": f"cookie 文件不存在: {cookie_path}，请先登录",
        }

    target_url = MESSAGING_URLS.get(platform, "")
    if not target_url:
        return {"ok": False, "error": f"未知平台: {platform}"}

    def _bg_run() -> None:
        loop = asyncio.new_event_loop()
        asyncio.set_event_loop(loop)
        try:
            loop.run_until_complete(
                _open_and_keep_alive(platform, account_name, cookie_path)
            )
        except Exception:
            pass  # Browser already shown or error; don't crash the thread

    t = threading.Thread(target=_bg_run, daemon=True)
    t.start()

    return {
        "ok": True,
        "platform": platform,
        "account": account_name,
        "message": f"已打开 {PLATFORM_LABELS.get(platform, platform)} 页面 ({target_url})，请在新窗口中操作。操作完成后可关闭窗口。",
    }
