PLATFORM_SCRAPERS = {
    "douyin": _scrape_douyin,
    "xiaohongshu": _scrape_xiaohongshu,
    "shipinhao": _scrape_tencent,
    "tencent": _scrape_tencent,
}


async def get_all_notifications(
    accounts: list[dict],  # each: {"platform": "douyin", "loginName": "xxx"}
) -> list[dict]:
    """Scrape notifications for all accounts in parallel."""
    tasks = []
    for acc in accounts:
        platform = acc.get("platform", "")
        login_name = acc.get("loginName", "")
        scraper = PLATFORM_SCRAPERS.get(platform)
        if scraper and login_name:
            tasks.append(_scrape_one(acc, scraper, login_name))

    if not tasks:
        return []

    results = await asyncio.gather(*tasks, return_exceptions=True)
    output = []
    for r in results:
        if isinstance(r, Exception):
            output.append({"logged_in": False, "counts": {}, "error": str(r)})
        else:
            output.append(r)
    return output


async def _scrape_one(acc: dict, scraper, login_name: str) -> dict:
    result = await scraper(login_name)
    result["platform"] = acc.get("platform", "")
    result["alias"] = acc.get("alias", "")
    result["loginName"] = login_name
    return result


def get_notifications_sync(accounts: list[dict]) -> list[dict]:
    """Synchronous wrapper for use in non-async code (e.g. server.py)."""
    return asyncio.run(get_all_notifications(accounts))


