def _cookie_dict(account_file: Path) -> dict[str, str]:
    """Convert Playwright storage_state cookies into a plain name→value dict."""
    if not account_file.exists():
        return {}
    data = json.loads(account_file.read_text(encoding="utf-8"))
    return {c["name"]: c["value"] for c in data.get("cookies", [])}


def _set_init_script(context):
    """Reuse social-auto-upload's stealth script injection."""
    spec = importlib.util.spec_from_file_location(
        "base_social_media",
        str(SAU_ROOT / "utils" / "base_social_media.py"),
    )
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod.set_init_script(context)


