async def _scrape_douyin(account_name: str) -> dict:
    cookie_path = account_file("douyin", account_name)
    if not cookie_path.exists():
        return {"logged_in": False, "counts": {}, "error": "cookie 文件不存在，请先登录"}

    async with async_playwright() as pw:
        browser = await pw.chromium.launch(**launch_kwargs(headless=True))
        try:
            context = await browser.new_context(storage_state=str(cookie_path))
            context = await _set_init_script(context)
            page = await context.new_page()
            await page.goto(
                "https://creator.douyin.com/creator-micro/home", timeout=30000
            )
            await page.wait_for_timeout(3000)

            # Detect login state
            if "/login" in page.url:
                return {"logged_in": False, "counts": {}, "error": "抖音 cookie 已过期，请重新登录"}

            counts: dict[str, int] = {}

            # Top-nav notification bell badge
            badge_selectors = [
                'span.notify-num',
                'span.badge',
                'span[class*="badge"]',
                'sup.ant-badge-count',
                'span.ant-badge-count',
                'span[class*="unread"]',
                'span[class*="count"]',
            ]
            for sel in badge_selectors:
                try:
                    el = page.locator(sel).first
                    if await el.count() and await el.is_visible():
                        text = (await el.text_content() or "").strip()
                        if text.isdigit():
                            counts["notifications"] = int(text)
                            break
                except Exception:
                    continue

            # Sidebar / specific tabs
            sidebar_items = {
                "私信": "messages",
                "评论": "comments",
                "@我": "mentions",
                "点赞": "likes",
                "通知": "notifications",
            }

            for label, key in sidebar_items.items():
                try:
                    item = page.get_by_text(label, exact=True).first
                    if await item.count():
                        parent = item.locator("..")
                        badge = parent.locator('span[class*="badge"], span[class*="count"], span[class*="num"]').first
                        if await badge.count():
                            text = (await badge.text_content() or "").strip()
                            if text.isdigit():
                                counts[key] = int(text)
                except Exception:
                    continue

            return {"logged_in": True, "counts": counts}

        except Exception as exc:
            return {"logged_in": False, "counts": {}, "error": str(exc)}
        finally:
            await browser.close()


